<h3 class="custom_header">Teemu Halmi - DIN17SP</h3>
<p>This little application is meant to serve as a gamemaster helper in the Middle-Earth
  Roleplaying Game. You can read more about MERP <a href="https://en.wikipedia.org/wiki/Middle-earth_Role_Playing">
  here</a>. <p>

<p>There are two different types of users, administrator and a regular user. This account is
  admin account. Administrator can create new users, delete users, scenarios and NPCs
  (Non-Player character). To get the full benefit of this application you need to login
  as a regular user. As a regular user you can create NPCs, scenarios and use Combat
  Helper. Feel free to experiment with diffrent functionalities.<P>

